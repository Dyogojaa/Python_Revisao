
# Operações Condições no Python
# if / elif / else
# se / se não se / se não

condicao = False
condicao2 = True
condicao3 = False
condicao4 = False
  
if condicao:
    print("Esse é o código do If")
elif condicao2:
    print("Esse é o código da Condição 2")        
elif condicao3:
    print("Esse é o código da Condição 3")        
elif condicao4:
    print("Esse é o código da Condição 4")                
else:
    print("Esse é o código do else")
        

