
# Operações Condições no Python
# if / elif / else
# se / se não se / se não

entrada = input('Você quer "entrar" ou "sair" ? ')

if entrada == "entrar":
    print("Você entrou no Sistema")
elif entrada == "sair":    
    print("Você saiu no Sistema")
else :
    print("Você não digitou nem entrar e nem sair")        
